const display = document.getElementById('analyticsDisplay');
let index = 0;

function typeReport(text, callback) {
  display.textContent = ''; // Clear previous text
  display.className = 'text-xs text-white bg-[#2B2D36] p-6 rounded-2xl whitespace-pre-wrap font-mono leading-relaxed w-[100%] h-[500px] overflow-hidden';

  let i = 0;
  const chunkSize = Math.floor(Math.random() * 6) + 5; // 5â€“10 chars
  const cursor = '|';
  let currentText = '';

  const interval = setInterval(() => {
    if (i < text.length) {
      const chunk = text.slice(i, i + chunkSize);
      i += chunk.length;
      currentText += chunk;
      display.textContent = currentText + cursor;
    } else {
      clearInterval(interval);
      display.textContent = currentText; // remove cursor
      setTimeout(() => eraseReport(callback), 3000);
    }
  }, 30);
}

function eraseReport(callback) {
  const lines = display.textContent.split('\n');
  let currentLines = [...lines];

  const interval = setInterval(() => {
    if (currentLines.length > 0) {
      currentLines.pop();
      display.textContent = currentLines.join('\n') + '\n|';
    } else {
      clearInterval(interval);
      setTimeout(callback, 300);
    }
  }, 50);
}

function nextReport() {
  typeReport(reports[index], () => {
    index = (index + 1) % reports.length;
    nextReport();
  });
}

document.addEventListener('DOMContentLoaded', nextReport);
